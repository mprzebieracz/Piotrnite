    private MethodSpec getGetSendable(Iterable<FieldSpec> fieldSpecs, TypeMirror genericTypePar) {
        StringJoiner args = new StringJoiner(", ");
        List<Object> formatArgs = new ArrayList<>();
        formatArgs.add(genericTypePar);

        fieldSpecs.forEach(field -> {
            args.add("$N");
            formatArgs.add(field.name());
        });

        var methodBuilder = MethodSpec.methodBuilder(CodegenConfig.getSendableMethodName)
                .addModifiers(PUBLIC, FINAL)
                .addAnnotation(Override.class)
                .returns(ClassName.get(genericTypePar));

        String format = "return new $T(" + args + ")";

        var codeBlock = CodeBlock.of(format, formatArgs.toArray());
        methodBuilder.addStatement(codeBlock);

        return methodBuilder.build();
    }

